<h1>Veelgestelde vragen</h1>

	<p>De Dierenzoeker is in de eerste plaats gericht op kinderen in de leeftijd van ongeveer 7-12 jaar. Je kan er Nederlandse dieren in vinden die je in en rond het huis tegenkomt.</p>

	<p>Op deze pagina vind je antwoord op enkele veelgestelde vragen over de Dierenzoeker.</p>

	<div class="faq-question">1. Hoe werkt de Dierenzoeker?</div>

	<div class="faq-answer">
	De Dierenzoeker toont een verzameling veel voorkomende huis-, tuin- en keukendieren in Nederland. Via het beantwoorden van vragen over het door jou gevonden dier (bijv. over kleur, gedrag, aantal poten) wordt de selectie dieren steeds kleiner. Het maakt niet uit met welke vraag je begint en je hoeft ze ook niet allemaal te beantwoorden. Soms is het mogelijk om meerdere antwoorden op een vraag te geven (bijv. de kleur - een dier kan zowel rode als groene kleuren hebben). Zo vind je jouw dier hopelijk snel. Klik op de naam van jouw dier voor meer informatie. Je kunt geselecteerde kenmerken ook weer verwijderen en daarna opnieuw beginnen met zoeken. Mocht het toch niet lukken je dier te vinden, kijk dan bij de vragen hieronder.
	</div>

	<div class="faq-question">2. Welke dieren kan ik vinden met de Dierenzoeker?</div>

	<div class="faq-answer">
		<p>In de Dierenzoeker vind je de meest voorkomende Nederlandse huis-, tuin en keukendieren. Het gaat om dieren uit de directe leefomgeving van mensen. Dieren in de stadsnatuur, zoals tuinen en parken, maar ook binnenshuis (de spin in je badkamer, de pissebed in de kelder).</p>

		<p>En... de Dierenzoeker breidt nog steeds uit! In de loop van 2014 worden er nog dieren toegevoegd. In totaal komen er 400 dieren in de Dierenzoeker.</p>

		<p>Op dit moment kun je de volgende dieren determineren: vogels, zoogdieren, vliegen en muggen, spinnen, pissebedden, duizend- en miljoenpoten, vlinders, bijen en hommels, mieren, amfibieÃ«n (kikkers, padden, salamanders), kevers, wormen, slakken, vissen, wespen, zweefvliegen, libellen en allerlei slootbeestjes.</p>

	</div>

	<div class="faq-question">3. Komen er nog meer dieren in de Dierenzoeker?</div>

	<div class="faq-answer">
		<p>Jazeker! Er komen in totaal zo'n 400 dieren in de Dierenzoeker. Elke maand komen er nieuwe dieren bij. Dit gebeurt in de loop van 2014.</p>

		<p>De volgende diergroepen kun je nog verwachten: hooiwagens, sprinkhanen, andere kriebelbeestjes.</p>
	</div>

	<div class="faq-question">4. Welke dieren komen NIET in de Dierenzoeker?</div>

	<div class="faq-answer">In de Dierenzoeker zitten alleen Nederlandse dieren die je dichtbij huis kunt aantreffen. Buitenlandse dieren, zoals de panda of de leeuw, staan dus niet in de Dierenzoeker. Nederlandse dieren die heel zeldzaam zijn, zitten ook niet in de Dierenzoeker, net als dieren die je alleen ver van huis, bijvoorbeeld in de zee, kunt vinden.</div>

	<div class="faq-question">5. Ik zoek een dier, maar kan hem niet vinden. Wat nu?</div>

	<div class="faq-answer">
		Als je jouw dier niet kunt vinden in de Dierenzoeker, kijk dan eerst bij de antwoorden op de vragen hierboven. Als die antwoorden geen uitkomst bieden, maak dan een beschrijving en het liefst ook een foto van het dier en mail deze naar <a target="_blank" href="mailto:dierenzoeker@naturalis.nl">dierenzoeker@naturalis.nl</a>. Medewerkers van natuurhistorisch museum Naturalis in Leiden zullen dan proberen je dier te determineren, en beantwoorden je mail binnen meestal binnen 2 weken.
	</div>

	<div class="faq-question">6. Waar vind ik meer goede informatie over dieren?</div>

	<div class="faq-answer">
		<p>Op <a target="_blank" href="http://www.tuintelling.nl">Tuintelling.nl</a> vind je extra informatie over diergroepen die je in je tuin kunt tegenkomen, en hoe je die kunt herkennen (inclusief een <a target="_blank" href="http://www.tuintelling.nl/index.php?id=367">overzicht van mooie zoekkaarten</a> en <a target="_blank" href="http://www.tuintelling.nl/index.php?id=369">tips om je tuin diervriendelijk in te richten</a>).</p>

		<p>Voor een overzicht van alle in Nederland voorkomende dieren kun je ook kijken op het <a target="_blank" href="http://www.nederlandsesoorten.nl">Nederlands Soortenregister</a>. In de bibliotheek of boekwinkel kun je ook veel informatie vinden, bijv. in de <a target="_blank" href="http://www.natuurenboek.nl/details.aspx?lnPKID=3790">Tirion insectengids</a> of de <a target="_blank" href="http://www.natuurenboek.nl/details.aspx?lnPKID=9225">ANWB vogelgids</a>.</p>

		<p>Voor dieren uit het buitenland kun je kijken op de Engelstalige websites <a target="_blank" href="http://www.arkive.org">ARKive</a> of <a target="_blank" href="http://www.eol.org">Encyclopedia of Life</a>.</p>

	</div>

	<div class="faq-question">7. Kan ik waarnemingen uit mijn tuin doorgeven?</div>

	<div class="faq-answer">
		<p>Op <a target="_blank" href="http://www.tuintelling.nl/index.php?id=366">Tuintelling.nl</a> vind je per diergroep informatie over wat je met je eigen waarnemingen uit de tuin kunt doen en bij welke organisatie je daarvoor moet zijn.</p>
	</div>

	<div class="faq-question">8. Is er ook een Dierenzoeker smartphone app?</div>

	<div class="faq-answer">De Dierenzoeker is als Android app beschikbaar in Google Play (<a target="_blank" href="https://play.google.com/store/apps/details?id=nl.naturalis.dierenzoeker">klik hier</a>) en als iPhone/iPad app in de App Store (<a target="_blank" href="https://itunes.apple.com/nl/app/id699543364">klik hier</a>). Of zoek in de app stores op Dierenzoeker.</div>

	<div class="faq-question">9. De Dierenzoeker werkt niet goed op mijn smartphone. Hoe kan dat?</div>

	<div class="faq-answer">De Dierenzoeker is geoptimaliseerd voor smartphones met een aanraakscherm (touch screen). De Dierenzoeker is getest en werkend bevonden op de iPhone van Apple en enkele telefoons van Samsung en HTC met het Android besturingssysteem. De Dierenzoeker is niet geoptimaliseerd voor telefoons met een Symbian, Windows Mobile of Blackberry OS besturingssysteem.</div>


